package uk.ac.shef.oak.com4510.ui.map;

/**
 * Location class
 *
 * @author Feng Li, Ruiqing Xu
 *
 */
public class Coord {

    private double lon;
    private double lat;
    public void setLon(double lon) {
         this.lon = lon;
     }
     public double getLon() {
         return lon;
     }

    public void setLat(double lat) {
         this.lat = lat;
     }
     public double getLat() {
         return lat;
     }

}